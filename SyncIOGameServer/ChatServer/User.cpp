#include "User.h"


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void User::Connect(const User& user)
{
	mSessionIndex = user.mSessionIndex;
	mSessionUniqueId = user.mSessionUniqueId;
	mIndex = user.mIndex;
	mUid = user.mUid;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void User::Clear()
{
	mSessionIndex = -1;
	mSessionUniqueId = 0;
	mIndex = -1;
	mUid = 0;
}